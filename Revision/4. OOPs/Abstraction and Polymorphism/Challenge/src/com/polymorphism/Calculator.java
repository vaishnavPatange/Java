package com.polymorphism;

public class Calculator {

        public int add(int a, int b, int c){
                return  a+b+c;
        }

        public int add(int a, int b){
                return a+b;
        }

        public double add(double a, double b){
                return a+b;
        }

        public String add(String a, String b){
                return  a+b;
        }


}
